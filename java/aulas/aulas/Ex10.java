public class Ex10 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("FIm da contagem crescente!");

        for (int a = 10; a >= 0; a--) {
            System.out.println(a);
        }
        System.out.println("Fim da contagem decrescente!");

    }
}
